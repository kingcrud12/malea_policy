function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });

    // Handle special cases like tab title
    const titleKey = document.body.getAttribute('data-i18n-page-title');
    if (titleKey && translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
    }
}

function switchLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    applyTranslations(lang);

    // Update active state in switcher
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Update html lang attribute
    document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || (navigator.language.startsWith('fr') ? 'fr' : 'en');
    switchLanguage(savedLang);
});
